// Increment dan Decrement

fun main() {

    var angka1 = 10
    val hasil1 = ++ angka1 // pada proses ini hasil1 sudah bernilai angka1 ditambah 1
    println(hasil1)
    println(angka1)

    println()

    var angka2 = 20
    val hasil2 = angka2 ++ // pada proses ini hasil2 baru mengambil nilai awal angka2 sebelum ditambah 1
    println(hasil2)
    println(angka2)

    println()

    var angka3 = 30
    val hasil3 = -- angka3 // pada proses ini hasil3 sudah bernilai angka3 dikurang 1
    println(hasil3)
    println(angka3)

    println()

    var angka4 = 20
    val hasil4 = angka4 -- // pada proses ini hasil4 baru mengambil nilai awal angka2 sebelum dikurang 1
    println(hasil4)
    println(angka4)
    
}
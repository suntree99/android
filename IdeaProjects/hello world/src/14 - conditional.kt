// Operator Kondisional

fun main() {

    val nilaiPertama = 10
    val nilaiKedua = 8

    println(nilaiPertama < nilaiKedua)
    println(nilaiPertama > nilaiKedua)
    println(nilaiPertama == nilaiKedua)
    println(nilaiPertama != nilaiKedua)
    println(nilaiPertama <= nilaiKedua)
    println(nilaiPertama >= nilaiKedua)

}
// Variabel Float
// Bernilai : Desimal atau Pecahan

fun main() {

    // Eksplisit
    val emas : Float
    emas = 7f // diberikan f untuk menginput tipe float

    // Implisit
    val perak = 2.5f // begitupun untuk menginput angka pecahan

    println("Saya memiliki emas $emas gram dan perak $perak gram")
}
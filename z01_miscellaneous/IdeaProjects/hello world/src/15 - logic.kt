// Operator Logika

fun main() {

    // And (&&)
    println( 1<2 && 3<4 )
    println( 1<2 && 3>4 )
    println( 1>2 && 3<4 )
    println( 1>2 && 3>4 )

    println()

    // Or (||)
    println( 1<2 || 3<4 )
    println( 1<2 || 3>4 )
    println( 1>2 || 3<4 )
    println( 1>2 || 3>4 )

    println()

    // Not (!)
    println( 1 != 2 )
    println( 2 != 2 )

}